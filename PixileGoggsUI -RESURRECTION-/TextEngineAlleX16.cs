    public static void PrintAlleX16Words(string content, int pixelLimit, int startX, int startY, int color)
    {
        
        string result = "";
        content = content.Replace("§", "\n");
        content += " ";
        int pixelLength = pixelLimit - pixelLimit % 10;
        for (int i = 0; i < content.Length; i++)
        {
            result += Get16X16Font(content[i]);
        }
        

        int currentX = startX;
        int currentY = startY;
        int currentLineCharIndex = 0;
        int currentLineIndex = 0;
        int contentIndex = 0;
        using StreamWriter fs = new StreamWriter("savefile.txt",true);
        for (int stringIndex = 0; stringIndex < result.Length-144; stringIndex++)
        {
            if (currentX >= pixelLength)//<--line brake
            {
                currentLineCharIndex = 1;
                currentLineIndex++;
                currentX = startX+7*currentLineCharIndex;
                currentY = startY-17*currentLineIndex;
            }
            else 
            if (content[contentIndex] == '\n')//<--force line brake
            {
                currentLineCharIndex = 1;
                currentLineIndex++;
                currentX = startX+7*currentLineCharIndex;
                currentY = startY-17*currentLineIndex;
                contentIndex++;
            }
            else
            if ((stringIndex + 0) % 144 == 0)//<--switch to the next char
            {
                currentLineCharIndex++;
                currentY = startY-17*currentLineIndex;
                currentX = startX+7*currentLineCharIndex;
                contentIndex++;
            }
            else
            if((stringIndex + 0) % 9 == 0)//<--single char line brake
            {
                currentY--;
                currentX = startX+7*currentLineCharIndex;
            }
            else
            {
                currentX++;
            }
            
            
            if (result[stringIndex] == '0')
            {
                
            }
            else if (result[stringIndex] == '1')
            {
                fs.WriteLine($"6,{color},{currentX},{currentY},0,0");
            }
        }
        fs.WriteLine("\n");
        fs.Close();
    }
